//JavaScript Arithmetic Operators
/*

Operator	Description
+	         Addition
-	         Subtraction
*	         Multiplication
**	         Exponentiation (ES2016)
/	         Division
%	         Modulus (Remainder)
++	         Increment
--	         Decrement

*/

// + operator
let a = 5;
let b = 2;
let c = a + b;
console.log(c);

// - operator
let m = 5;
let n = 2;
let d = m - n;
console.log(d);

// * multiplication
let k = 5;
let j = 2;
let h = k * j;
console.log(h);

// / division 
let l = 5;
let s = 2;
let q = l / s;
console.log(q);

// the modulus operator (%) returns the division remainder.
let g = 5;
let w = 2;
let r = q % w;
console.log(r);

//The increment operator (++) increments numbers.
let x = 4;
x++; // this should increase the value of x by 1 so now x will be 5
console.log(x);

//The decrement operator (--) decrements numbers.
let y = 4;
y--; // this should decrease the value of y by 1 so now x will be 3
console.log(y);

//** exponentiation
console.log(3 ** 4);//produces 81
console.log(3 ** 2); //produces 9