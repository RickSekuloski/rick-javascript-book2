//re-export features

const calcAddition = (n,m)=>{
    return n + m; 
}
const calcSubstraction = (n,m)=>{
    return n - m; 
}
const calcMultiplication = (n,m)=>{
    return n * m; 
}
const calcDivision = (n,m)=>{
    return n / m; 
}

export {calcAddition, calcSubstraction, calcMultiplication, calcDivision};